#  Define a function named greet that takes a name as an argument and prints a greeting.
# Define a function named 'greet' that takes one parameter 'name'
def greet(name):
    # Print a greeting message that includes the name provided
    print(f"Hello, {name}!")

# Prompt the user to enter a name and store the input in the variable 'name'
name = input("Enter the name: ")

# Call the 'greet' function
greet(name)